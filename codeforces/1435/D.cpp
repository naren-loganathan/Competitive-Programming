#include <iostream>
#include <string>
#include <vector>
#include <stack>
#include <algorithm>
using namespace std;

typedef long long int ll;
typedef long double ld;
#define mod 1000000007
#define N 100005
#define mp make_pair
#define ub upper_bound
#define lb lower_bound
#define fi first
#define se second

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int t = 1; 
    //cin >> t;
    while (t--) {
        int n, x; cin >> n;
        bool possible = 1;
        vector < pair <char, int> > transactions;
        for (int i = 0; i < 2 * n; i++) {
            char c;
            cin >> c;
            if (c == '+') {
                transactions.push_back(mp('+', 0));
            }
            else {
                cin >> x;
                transactions.push_back(mp('-', x));
            }
        }
        stack <int> stk; vector <int> ans;
        for (int i = 2 * n - 1; i >= 0; i--) {
            if (transactions[i].fi == '+') {
                if (stk.empty()) {
                    possible = 0; break;
                }
                ans.push_back(stk.top());
                stk.pop();
            }
            else {
                if (!stk.empty() && stk.top() < transactions[i].se) {
                    possible = 0; break;
                }
                stk.push(transactions[i].se);
            }
        }
        cout << (possible ? "YES\n" : "NO\n");
        if (possible) {
            for (int i = ans.size() - 1; i >= 0; i--) {
                cout << ans[i] << " ";
            }
            cout << "\n";
        }
    }
    return 0;
}